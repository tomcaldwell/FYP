package org.gt4j.annas.exception;

public class NoMoreIntegersException extends AnnasRuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5463830533782478536L;

	public NoMoreIntegersException() {
		super();
	}

	public NoMoreIntegersException(String message) {
		super(message);
	}

	public NoMoreIntegersException(Throwable cause) {
		super(cause);
	}

	public NoMoreIntegersException(String message, Throwable cause) {
		super(message, cause);
	}

}
